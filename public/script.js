const chatForm = document.getElementById('chat-form');
const chatInput = document.getElementById('bottom-input');

if (chatForm) {
    chatForm.addEventListener('submit', async (e) => {
        
        
        e.preventDefault(); 

        const message = chatInput.value.trim();
        if (!message) return;

        
        chatInput.value = '';

        
        const answerBox = appendChatBlock(message);

        try {
          
            const modelSelect = document.getElementById('model-select');
            const currentModel = modelSelect 
                                 ? modelSelect.value 
                                 : (typeof INITIAL_DATA !== 'undefined' && INITIAL_DATA.model ? INITIAL_DATA.model : 'gemini-2.5-flash');

            
            const data = await handleChat(message, currentModel, currentChatId);

            if (data.chatId && !currentChatId) {
                currentChatId = data.chatId;
                window.history.pushState(null, '', '/polychat/' + currentChatId);
            }

            //ui call
            if (answerBox) {
                answerBox.innerHTML = data.reply;
            }

        } catch (error) {
            console.error("Chat Error:", error);
            if (answerBox) {
                answerBox.innerHTML = "<p style='color:red;'>Error connecting to server. Please try again.</p>";
            }
        }
    });
}

